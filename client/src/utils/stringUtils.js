export function mapColorToRGB(color) {
  switch (color) {
    case "beige":
      return "#CFC2B7";
    case "black":
      return "#191919";
    case "blue":
      return "#2441AC";
    case "brown":
      return "#39221A";
    case "gray":
      return "#777777";
    case "red":
      return "#D10000";
    case "pink":
      return "#EECCD3";
    case "purple":
      return "#224798";
    case "white":
      return "#F9F6F0";
    case "yellow":
      return "#FEC401";
    default:
      break;
  }
}
